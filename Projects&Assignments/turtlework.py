import turtle as t
t.screensize(800,600,"black")
t.speed(0)
t.hideturtle()
def initialize():
    t.penup()
    t.home()
    t.right(90)
    t.fd(100)
    t.right(90)
    t.fd(300)
    t.right(90)
def move():
    initialize()
    t.fd((30*13)-(0.0538*30*13)*0.7-30)
    t.right(90)
    t.fd(30*13/12-(0.0616*30*13*0.5)*1.55)
def star(x):
    t.color("white","white")
    t.fd(x)
    t.begin_fill()
    for i in range(4):
        t.rt(144)
        t.fd(x)
    t.rt(144)
    t.end_fill()
    t.penup()
    t.fd(30*13*1.9*0.4*2/12)
    t.pendown()
def star50(x,y):
    for j in range(y):
        star(x)
def red(t,x,y):
    t.pencolor('red')
    t.fillcolor('red')
    t.begin_fill()
    t.fd(x)
    t.rt(90)
    t.fd(y)
    t.rt(90)
    t.fd(x)
    t.rt(90)
    t.fd(y)
    t.end_fill()
def white(t,x,y):
    t.pencolor('white')
    t.fillcolor('white')
    t.begin_fill()
    t.fd(x)
    t.rt(90)
    t.fd(y)
    t.rt(90)
    t.fd(x)
    t.rt(90)
    t.fd(y)
    t.end_fill()
def lines(x):
    w=x*13*1.9
    for i in range(12):
        if i%2==1:
            white(t,w,x)
            t.pencolor('red')
            t.fd(x)
            t.rt(90)
        else:
            red(t,w,x)
            t.pencolor('white')
            t.fd(x)
            t.rt(90)
    red(t, w, x)
initialize()
t.right(90)
lines(30)
initialize()
t.fd(150)
t.color("midnightblue","midnightblue")
t.begin_fill()
t.fd(30*7)
t.rt(90)
t.fd(30*13*1.9*0.4)
t.rt(90)
t.fd(30*7)
t.rt(90)
t.fd(30*13*1.9*0.4)
t.rt(90)
t.end_fill()
move()
t.pendown()
for i in range(5):
    star50(0.0616*30*13,6)
    move()
    t.penup()
    t.right(90)
    t.fd(30*13*0.0538*(i+1)*2)
    t.left(90)
    t.pendown()
for i in range(4):
    move()
    t.penup()
    t.right(90)
    t.fd(0.0538*13*30+30*13*0.0538*i*2)
    t.left(90)
    t.fd(0.0616*30*13)
    star50(0.0616*30*13,5)
initialize()
t.left(180)
t.fd(100)
t.left(90)
t.fd(400)
t.pendown()
t.mainloop()


